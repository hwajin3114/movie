package com.cbox.movie.vo;

public class TheaterVO {
	private int thNum; // 상영관 번호
	private String thName; // 상영관명
	private int thMax; // 최대인원

	public int getThNum() {
		return thNum;
	}

	public void setThNum(int thNum) {
		this.thNum = thNum;
	}

	public String getThName() {
		return thName;
	}

	public void setThName(String thName) {
		this.thName = thName;
	}

	public int getThMax() {
		return thMax;
	}

	public void setThMax(int thMax) {
		this.thMax = thMax;
	}
}
